''' Imports '''
from django import forms
from .models import Profile


class ProfileForm(forms.ModelForm):
    ''' create form that can be edited by user '''
    class Meta:
        ''' What model we want to use and the fields we want the user to fill in '''
        model = Profile
        fields = ('user', 'owner', 'dog', 'address', 'phonenumber', 'info',)
